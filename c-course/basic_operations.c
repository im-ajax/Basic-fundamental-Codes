// Datatypes and variables ->
    /*lets start with datatypes and variables
      int -> for integer 
      float -> float values
      long long -> for bigger integers
      double/long float -> for bigger float values
      char -> for character;
      string -> used for working with strings

      format specifier:
      int -> %d
      float -> %f
      long long int/long long -> %lld
      long float/double -> %lf
      char -> %c
      string -> %s
    */

    // variables ->
    /*
        for variables we can choose any type of meaningful names
        but there are some rules :

        prohibitions :
        -> variables cannot start with numbers : 12variable
        -> cannot start with special characters : #variable
        -> cannot contain spaces : this is a variable
        -> cannot be in quotation : "variable name"

        rules:
        -> start with mixed order letters : variable/Variable
        -> user underscode instead of spaces : variable_name
        -> if needed to add numbers then add in last : variable1,variable2
        -> you can mix uppercase lowercase : varialbe,VaRiable
    */



